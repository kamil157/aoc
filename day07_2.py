def thrusters(s):
    perm = itertools.permutations(range(5, 10))
    best = 0

    for p in perm:
        intcodes = []
        for i in range(5):
            intcode = Intcode(s)
            intcodes.append(intcode)
            intcode.input(p[i])

        signal = 0
        prev_signal = 0
        i = 0
        while True:
            intcodes[i % 5].input(signal)
            signal = intcodes[i % 5].run()
            if signal == 'halt':
                best = max(best, prev_signal)
                break
            prev_signal = signal
            i += 1

    return best


